# Program that receives a value_x from the user and performs math operations.

# Variable capturing user's input.
valor_x = int(input("Input any integer number: "))

# For loop block of code.
for i in range(1, valor_x + 1):
    print(f"Line number: {i} / Value squared: {i**2} / Cubed value: {i**3}.")

# Ending block of code.
print()
print("-----------------------------------------------------------------")
print("End of program.")
